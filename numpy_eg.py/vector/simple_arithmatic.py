import numpy as np      

# 1.
# Addition

arr_1 = np.array([1,2,3,4,5])
arr_2 = np.array([5,4,3,2,1])
print(np.add(arr_1,arr_2))

# 2.
# Subtraction

arr_1 = np.array([1,2,3,4,5])
arr_2 = np.array([5,4,3,2,1])
print(np.subtract(arr_1,arr_2))

# 3.
# multiplication

arr_1 = np.array([1,2,3,4,5])
arr_2 = np.array([5,4,3,2,1])
print(np.multiply(arr_1,arr_2))

# 4.
# Division

arr_1 = np.array([1,2,3,4,5])
arr_2 = np.array([5,4,3,2,1])
print(np.divide(arr_1,arr_2))

# 5.
# Power

arr_1 = np.array([1,2,3,4,5])
arr_2 = np.array([5,4,3,2,1])
print(np.power(arr_1,arr_2))

# 6.
# remainder() and mod()

arr_1 = np.array([1,2,3,4,5])
arr_2 = np.array([5,4,3,2,1])
print(np.mod(arr_1,arr_2))

# 7.
# quotient and mod in same func

arr_1 = np.array([1,2,3,4,5])
arr_2 = np.array([5,4,3,2,1])
print(np.divmod(arr_1,arr_2))

# 8.
# Absolute value

arr_new = np.array([-1,-2,-3,-4])
print(np.absolute(arr_new))

